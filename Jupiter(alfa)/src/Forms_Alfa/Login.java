    boolean loadDatos(String user, String pwd){
        try{
            registro = exRef.Login(user, pwd);            
            u = new Data(Integer.parseInt(this.registro[0]), this.registro[4]);
            return true;
        }catch(Exception e){
            return false;            
        }        
            
    }
    
    class Hilo extends Thread{
        Login log;
        Hilo(Login log){
            this.log = log;
        }
        
        public void run(){
            setLoginbtnName(false);            
            try{
                if(txtUser.getText().length() != 0){ 
                    if(loadDatos(txtUser.getText(), new String(txtPwd.getPassword()))){
                        if(registro[3].equals("Normal")){
                            FNormal fn = new FNormal();
                            fn.setTitle("Bienvenido(a)");
                            fn.setData(u);
                            fn.setVisible(true);
                        }else if(registro[3].equals("Administrador")){                        
                            FAdmin fa = new FAdmin();
                            fa.setTitle("Bienvenido(a)");
                            fa.setData(u);
                            fa.setVisible(true);
                        }

                        log.dispose();
                    }else{
                        setLoginbtnName(true);
                        JOptionPane.showMessageDialog(null, "El usuario y/o la contrase침a no existen.\nVuelve a Intentarlo.", "Error al entrar", JOptionPane.ERROR_MESSAGE);            
                    }
                }else{
                    setLoginbtnName(true);
                    JOptionPane.showMessageDialog(null, "No se ha ingresado el usuario.\nVuelve a Intentarlo.", "Error al Entrar", JOptionPane.ERROR_MESSAGE);            
                }
            }catch(Exception e){}
        }
    }
    
    void setLoginbtnName(boolean value){
        if(value){
            jLabel3.setText("Iniciar Sesi칩n");
            btnEntrar.setText("Entrar");
        }else{
            jLabel3.setText("Conectando....");
            btnEntrar.setText("Cancelar");
        }
    }
